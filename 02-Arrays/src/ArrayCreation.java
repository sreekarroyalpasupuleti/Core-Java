class ArrayCreation {

    public static void main(String... args){

        int [ ]  arr  = new int [ 4 ] ;

        arr[0] = 121;
        arr[1] = 122;
        arr[2] = 123;
        arr[3] = 103;

        System.out.println ( arr [ 0 ] ) ;
        System.out.println ( arr [ 1 ] ) ;
        System.out.println ( arr [ 2 ] ) ;
        System.out.println ( arr [ 3 ] ) ;

    }
}
//Output:
//121
//122
//123
//103