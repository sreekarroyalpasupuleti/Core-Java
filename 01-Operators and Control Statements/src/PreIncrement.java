public class PreIncrement {

        public static void main(String... args){

            int a = 78;
            System.out.println ( ++ a );  // it will become 79 then it will print

            ++ a ;  // it will become 80

            System.out.println(++a);  // it will become 81 then it will print

            System.out.println(a); // it will print 81
        }


}
